// Ждём, пока документ полностью загрузится
$(document).ready(function () {

  // ============================
  // ИНИЦИАЛИЗАЦИЯ OWL CAROUSEL
  // ============================

  $('.owl-carousel').owlCarousel({ // Выбираем элемент с классом .owl-carousel
    items: 1,               // Количество отображаемых слайдов одновременно (по одному)
    loop: true,             // Слайды будут зациклены (последний переходит снова в первый)
    margin: 10,             // Отступ между слайдами в пикселях
    nav: true,              // Показывать стрелки "вперёд/назад"
    dots: true,             // Показывать навигационные точки снизу
    autoplay: true,         // Включить автоматическую прокрутку слайдов
    autoplayTimeout: 3000,  // Время между слайдами в автопрокрутке (3000 мс = 3 секунды)
    autoplayHoverPause: true // При наведении мышки на слайдер автопрокрутка ставится на паузу
  });


  // ============================
  // ИНИЦИАЛИЗАЦИЯ FANCYBOX
  // ============================

  // Привязываем Fancybox ко всем элементам с атрибутом data-fancybox="gallery"
  Fancybox.bind("[data-fancybox='gallery']", {
    
    Toolbar: {
      // Показывать элементы панели инструментов
      display: [
        "zoom",        // Кнопка увеличения
        "fullscreen",  // Показ в полноэкранном режиме
        "thumbs",      // Превьюшки снизу
        "close"        // Кнопка закрытия
      ]
    },

    Thumbs: {
      autoStart: true // Превью (миниатюры) открываются сразу при запуске FancyBox
    }
  });

});


